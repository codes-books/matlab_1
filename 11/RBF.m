%通用径向基函数网络
%其在逼近能力,分类能力,学习速度方面均优于BP神经网络
%设计一个径向基函数网络，网络有两层,隐层为径向基神经元,输出层为线性神经元
%绘制隐层神经元径向基传递函数的曲线
p=-1:0.001:1;
a=radbas(p);
plot(p,a)
%应用newrb()函数构建径向基网络的时候,可以预先设定均方差精度eg以及散布常数sc
eg=0.0001;%误差
sc=100;   %总步数
net=newrb(p,a,eg,sc);
%网络测试
plot(p,a,'k:')
title('目标向量和输出向量曲线')
xlabel('输入')
ylabel('输出和目标');
X=-1:0.001:1;
Y=sim(net,p);
hold on
plot(X,Y);
hold off
legend('目标','输出')
